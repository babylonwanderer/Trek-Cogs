class Rules:
    def __init__(self, bot):
        self.bot = bot

    @commands.command()
    async def RoA(self, num: int=None):
        """RoA"""
        if num:
            if num < 1:
                await self.bot.say('Just how dumb are you exactly?')
                return
            if num > 286:
                await self.bot.say('There are only 286 Rules of Acquisition.')
                return
            await self.bot.say(":orange_book:Rules of Acquisition # {}: {}".format(num, rules[num]))
            return
        rule = randchoice(list(rules.keys()))
        await self.bot.say(":orange_book:Rules of Acquisition # {}: {}".format(rule, rules[rule]))


def setup(bot):
    n = Rules(bot)
    bot.add_cog(n)
